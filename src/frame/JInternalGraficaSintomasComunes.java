    private void cbSintomasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbSintomasActionPerformed
        dg = pg.buscarSintomas(cbSintomas.getSelectedItem().toString());
        int sintoma = dg.getCantidadSintomasReportados();
        System.out.println(sintoma);
        if (sintoma != 0) {
            JPanelGraficoSintomasComunes.removeAll();
            JPanelGraficoSintomasComunes.revalidate();
            JPanelGraficoSintomasComunes.repaint();
            buscarSintomas();
        } else {
            JPanelGraficoSintomasComunes.removeAll();
            JPanelGraficoSintomasComunes.revalidate();
            JPanelGraficoSintomasComunes.repaint();
            JOptionPane.showMessageDialog(null, "No hay paciente con este sintoma", "Buscar sintomas", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_cbSintomasActionPerformed

