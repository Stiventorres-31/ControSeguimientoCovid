    private void buscarSintomas() {

        dg = pg.buscarSintomas(cbSintomas.getSelectedItem().toString());
        int sintoma = dg.getCantidadSintomasReportados();

        //sacar de la base de datos tu los borras.
        DefaultCategoryDataset datos = new DefaultCategoryDataset();//Establecer los valores(supongo que tambien lo quitas)

        datos.setValue(sintoma, "Cantidad", cbSintomas.getSelectedItem().toString());//Los datos almacenados
        //datos.setValue(f2, "Cantidad", fecha2);

        JFreeChart grafico_barras = ChartFactory.createBarChart3D(
                "Cantidad de personas con este sintoma", //NOMBRE DEL GRAFICO
                "Sintoma", //NOMBRE DE LAS BARRAS O COLUMNAS
                "Cantidad", //NOMBRE DE NUMERACION
                datos, //datos de barras
                PlotOrientation.VERTICAL, //ORIENTACION
                true, //barra individual por colores
                true, //herramientas
                false //url del grafico
        );

        ChartPanel panel = new ChartPanel(grafico_barras);  //Creando un panel tipo chart
        panel.setMouseWheelEnabled(true);                   //Mouse Habilitado (Ruenda del raton para manipular)
        panel.setPreferredSize(new Dimension(732, 437));     //Dimension

        JPanelGraficoSintomasComunes.setLayout(new BorderLayout());         //Asignando al panel donde va el grafico con bordes
        JPanelGraficoSintomasComunes.add(panel, BorderLayout.NORTH);
        JPanelGraficoSintomasComunes.setEnabled(false);
        pack();//Estos son necesarios para el grafico
        repaint();
    }

