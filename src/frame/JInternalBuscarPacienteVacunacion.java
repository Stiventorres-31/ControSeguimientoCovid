    private void limpiarTabla() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i--;
        }
    }

    private void listarPacientesVacunados() {
        List<DatosVacunacion> listVa = pv.listar();
        modelo = (DefaultTableModel) tbMostrarFiltroPaciente.getModel();
        Object[] ob = new Object[8];
        for (int i = 0; i < listVa.size(); i++) {
            ob[0] = listVa.get(i).getPvc_cedula_paciente();
            ob[1] = listVa.get(i).getPvc_nombres_paciente();
            ob[2] = listVa.get(i).getPvc_codigo_vacuna();
            ob[3] = listVa.get(i).getPvc_nombre_vacuna();
            ob[4] = listVa.get(i).getPvc_laboratorio_vacuna();
            ob[5] = listVa.get(i).getPvc_dosis();
            ob[6] = listVa.get(i).getPvc_fecha_registro();
            ob[7] = listVa.get(i).getPvc_cedula_usuario();
            modelo.addRow(ob);
        }
        tbMostrarFiltroPaciente.setModel(modelo);
    }

